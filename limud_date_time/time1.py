from time import *

# נותן לי זמן נוכחי בשניות מאז 1/1/1970 בחצות
print(time())

# פקודה שנותנת זמן השהייה של התוכנית בשניות
sleep(0)

# נותן לי את הזמן בצורה מפורקת באובייקט
print(localtime())
print(gmtime())

# נותן לי תאריך ושעה כמחרוזת
print(asctime())

# מקבלת זמן בשניות מאז 1970 בחצות ומחזירה לי תאריך ושעה כמחרזת
print(ctime(1613872289))